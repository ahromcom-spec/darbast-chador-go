// تابع ساخت راهنمای ماژول‌ها بر اساس ماژول‌های اختصاص‌یافته
function getModuleUsageGuides(moduleKeys: string[], moduleNames: string[] = []): string {
  if (moduleKeys.length === 0) return '';
  
  let guides = `

═══════════════════════════════════════
📖 راهنمای کامل نحوه استفاده از ماژول‌های شما
═══════════════════════════════════════
در ادامه راهنمای کامل استفاده از ماژول‌هایی که به شما اختصاص داده شده آمده است. اگر کاربر درباره نحوه استفاده از هر ماژول سوال کرد، از این اطلاعات استفاده کن:
`;

  // Resolve custom module keys to base keys using key and name heuristics
  const resolveBaseKey = (key: string, name: string): string => {
    if (MODULE_USAGE_GUIDES[key]) return key;
    // For custom-* keys, match by module name
    if (name.includes('گزارش روزانه')) return 'daily_report';
    if (name.includes('منابع انسانی')) return 'hr_management';
    if (name.includes('حسابکتاب') || name.includes('کارکرد')) return 'personnel_accounting';
    if (name.includes('صورتحساب جامع مشتری')) return 'customer_comprehensive_invoice';
    if (name.includes('صورتحساب')) return 'my_invoice';
    if (name.includes('کارت') || name.includes('بانک')) return 'bank_cards';
    if (name.includes('ثبت‌نام') || name.includes('ثبت نام')) return 'site_registration';
    if (name.includes('حسابداری جامع')) return 'comprehensive_accounting';
    if (name.includes('آمار') || name.includes('بازدید')) return 'site_analytics';
    if (name.includes('رسانه')) return 'media_approval';
    if (name.includes('داربست') || name.includes('اجرایی')) return 'scaffold_execution_with_materials';
    return key;
  };

  const addedGuides = new Set<string>();
  for (let i = 0; i < moduleKeys.length; i++) {
    const key = moduleKeys[i];
    const name = moduleNames[i] || '';
    const baseKey = resolveBaseKey(key, name);
    if (MODULE_USAGE_GUIDES[baseKey] && !addedGuides.has(baseKey)) {
      guides += MODULE_USAGE_GUIDES[baseKey];
      addedGuides.add(baseKey);
    }
  }

  guides += `

═══════════════════════════════════════
💡 نکات عمومی استفاده از ماژول‌ها
═══════════════════════════════════════
- برای دسترسی به ماژول‌ها، از منوی «اهرم من» → «ماژول‌های من» در پروفایل خود استفاده کنید
- برای افزودن میانبر ماژول به صفحه نخست، ۳ ثانیه روی ماژول فشار دهید و نگه دارید
- اگر ماژول جدیدی نیاز دارید، از مدیر سیستم بخواهید آن را برای شما فعال کند
- هر ماژول ممکن است نام سفارشی داشته باشد که توسط مدیرعامل تعیین شده است
`;

  return guides;
}


// تابع برای گرفتن ماژول‌های کاربر
async function getUserModules(supabase: any, userId: string): Promise<{ moduleKeys: string[]; moduleNames: string[] }> {
  try {
    const { data: moduleData, error } = await supabase
      .from("module_assignments")
      .select("module_key, module_name")
      .eq("assigned_user_id", userId)
      .eq("is_active", true);
    
    if (error || !moduleData || moduleData.length === 0) {
      console.log('No modules found for user:', userId);
      return { moduleKeys: [], moduleNames: [] };
    }
    
    return {
      moduleKeys: moduleData.map((m: any) => m.module_key),
      moduleNames: moduleData.map((m: any) => m.module_name)
    };
  } catch (error) {
    console.error('Error fetching user modules:', error);
    return { moduleKeys: [], moduleNames: [] };
  }
}

