async function getUserRoles(supabase: any, userId: string): Promise<string[]> {
  try {
    const { data: roles, error } = await supabase
      .from("user_roles")
      .select("role")
      .eq("user_id", userId);
    
    if (error || !roles) return [];
    return roles.map((r: any) => r.role);
  } catch (error) {
    console.error('Error fetching user roles:', error);
    return [];
  }
}

// تابع برای بررسی آیا کاربر مدیرعامل است
async function isCEOUser(supabase: any, userId: string): Promise<boolean> {
  try {
    // بررسی نقش CEO
    const { data: ceoRole } = await supabase
      .from("user_roles")
      .select("role")
      .eq("user_id", userId)
      .eq("role", "ceo")
      .maybeSingle();
    
    if (ceoRole) return true;
    
    // بررسی شماره تلفن مدیرعامل
    const { data: profile } = await supabase
      .from("profiles")
      .select("phone_number")
      .eq("user_id", userId)
      .maybeSingle();
    
    const CEO_PHONE = "09124411494";
    if (profile?.phone_number === CEO_PHONE) return true;
    
    return false;
  } catch (error) {
    console.error('Error checking CEO status:', error);
    return false;
  }
}

